package com.bridgelabz.fellowship.model;

public class UserModel {
	private String name;
	private int buy_share;
	private int sell_share;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getBuy_share() {
		return buy_share;
	}

	public void setBuy_share(int buy_share) {
		this.buy_share = buy_share;
	}

	public int getSell_share() {
		return sell_share;
	}

	public void setSell_share(int sell_share) {
		this.sell_share = sell_share;
	}

}
